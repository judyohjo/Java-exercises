
public class AssignmentExample {

	public static void main(String[] args) {
		
		int a = 5, b = 5;
		
		a += 3; // a = a + 3;
		b =+ 3; // b = + 3;
		System.out.println("a: " + a);
		System.out.println("b: " + b);
		
		System.out.println("----------------");
		a -= 4;
		System.out.println(a);
		
		a *= 6;
		System.out.println(a);
		
		a /= 5; 
		System.out.println(a);
		
		a %= 3; 
		System.out.println(a);

	}

}
